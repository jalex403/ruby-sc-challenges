puts "How is the weather today"
weather = gets.chomp

case weather
when "sunny"
  puts "Put on some sunscreen and a hat"
when "cloudy"
  puts "It won't be hot or cold today"
when "foggy"
  puts "Take your flashlight to see through the fog"
when "snowy"
  puts "Put on your snow shoes"
when "rainy"
  puts "Take your umbrella and some rain boots"
end